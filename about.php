                                        <h3>BACKGROUND AND INTRODUCTION</h3>
                                        <p>SAFI began in 2002 as community-based organization that was set up to coordinate the food relief assistance in response to the severe famine that hit Malawi in that year. As time went by, the founders realized that provision of food relief items was not sustainable in the long-term. It was felt that a sustainable solution to food insecurity was to capacitate households so that they can produce all the food they need hence SAFI was established and commenced its operations in 2007 with its first cohort of 30 families. Today, SAFI has trained over 300 families on its campus in Madisi, Dowa district.</p><br>
                                        <p>Over the years, demand for SAFI programs grew exponentially. Most farmers expressed interest to be admitted into the program but it was not possible to increase intake beyond 40 families per year because of space limitations on the campus. However, in trying to respond to this demand, another organization was set up in 2013 called Brighter Future Initiative which later changed to Childrenâ€™s Brighter Future (CBF). The aim of CBF was to provide a compressed version of the SAFI training program to families who cannot come to SAFI due to space limitations. The CBF program is delivered in modular approach with three modules namely; backyard gardening, chicken farming and field crops. To date, CBF has managed to reach out to 8759 farm families from Lilongwe, Kasungu, Ntchisi, Dowa and Mchinji. In 2017, CBF was rebranded to become SAFI Extension Program. </p><br>
                                        <p>Educate the Children (ETC) is another sister program to the two programs. Its mandate is to provide scholarships to secondary and college going students. To date, ETC many students have completed secondary school and several other students have graduated from both public and private colleges with scholarship assistance from this program.</p><br>
